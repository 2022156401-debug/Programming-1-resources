// How an increment work 
#include <iostream>
using namespace std;

int main() {
    int i = 5;
    cout << "Using i++ (post-increment): " << i++ << endl;
    cout <<"w i++ incriment: "<< i << endl;
    cout <<"w i++ incriment: "<< ++i << endl;
    cout <<"w i++ incriment: "<< i << endl;    
return 0;
}